def _write(msg: str = "") -> None:
    """Write a line to stdout."""
    sys.stdout.write(msg + "\n")


def _print_kv(pairs: list[tuple[str, str]]) -> None:
    """Print key-value pairs in aligned format."""
    try:
        from arccli.formatting import print_kv

        print_kv(pairs)
    except ImportError:
        width = max(len(k) for k, _ in pairs) if pairs else 0
        for k, v in pairs:
            sys.stdout.write(f"  {k:<{width}}  {v}\n")


_USER_CONFIG_HEADER = """# Arc {pkg} config — user-wide overrides
# Generated by `arc init --tier {tier}`
#
# This file deep-merges OVER the packaged defaults. Values you set here win;
# anything you omit falls through to the packaged defaults shipped with the
# {pkg} package. Per-instance configs (e.g. team/<agent>/arcagent.toml) merge
# on top of this file. Env vars override everything.
"""


def _toml_value(val: Any) -> str:
    """Render a Python value as a TOML scalar/array literal."""
    if isinstance(val, bool):
        return "true" if val else "false"
    if isinstance(val, str):
        return f'"{val}"'
    if isinstance(val, (int, float)):
        return str(val)
    if isinstance(val, list):
        return "[" + ", ".join(_toml_value(v) for v in val) + "]"
    raise ValueError(f"Unsupported TOML value type: {type(val).__name__}")


def _emit_module_blocks(preset: dict[str, dict[str, Any]]) -> list[str]:
    """Render [modules.<name>] blocks from a tier preset."""
    lines: list[str] = []
    for module_name, settings in preset.items():
        lines.append(f"[modules.{module_name}]")
        for key, val in settings.items():
            lines.append(f"{key} = {_toml_value(val)}")
        lines.append("")
    return lines


def _generate_arcllm_toml(tier: str, provider: str = "anthropic") -> str:
    """Generate ~/.arc/arcllm.toml content for a given tier."""
    preset = _TIER_PRESETS[tier]
    lines = [
        _USER_CONFIG_HEADER.format(pkg="arcllm", tier=tier),
        "[defaults]",
        f'provider = "{provider}"',
        "temperature = 0.7",
        "max_tokens = 4096",
        "",
        "[vault]",
        'backend = ""',
        "cache_ttl_seconds = 300",
        'url = ""',
        'region = ""',
        "",
        *_emit_module_blocks(preset),
    ]
    return "\n".join(lines) + "\n"


def _arcagent_base_config(tier: str) -> dict[str, Any]:
    """User-wide arcagent DEFAULTS as a dict (so a blueprint can deep-merge under them).

    No agent identity — name/did live in team/<agent>/arcagent.toml. ``tier`` is already
    a security-vocab value (personal/enterprise/federal); there is no normalization fork.
    """
    return {
        "security": {"tier": tier, "validators": {"auto_run_agent_code": False}},
        "telemetry": {"enabled": True, "log_level": "INFO", "export_traces": False},
        "context": {
            "max_tokens": 200000,
            "prune_threshold": 0.70,
            "compact_threshold": 0.85,
            "emergency_threshold": 0.95,
        },
        "eval": {"provider": "", "model": "", "max_tokens": 1024, "temperature": 0.2},
        "session": {"retention_count": 50, "retention_days": 30},
    }


def _generate_arcagent_toml(tier: str, blueprint_name: str | None = None) -> tuple[str, str]:
    """Render ~/.arc/arcagent.toml. Returns ``(content, effective_tier)``.

    With ``blueprint_name`` the preset is verified + deep-merged UNDER the init defaults
    (defaults win over the blueprint), the tier floored by stringency-max, and the apply
    audited. Written concretely (dict -> TOML) so the layered CLI/gateway load reads it.
    """
    from arcagent.blueprints import dumps_toml

    base = _arcagent_base_config(tier)
    effective = tier
    if blueprint_name:
        from arcagent.blueprints import apply_blueprint, resolve_blueprint

        from arccli.commands.blueprint import audit_apply

        bp = resolve_blueprint(blueprint_name, tier=tier)
        base = apply_blueprint(bp, base, deployment_tier=tier)
        effective = str(base.get("security", {}).get("tier", tier))
        audit_apply(bp, base, Path.home() / ".arc")
    header = _USER_CONFIG_HEADER.format(pkg="arcagent", tier=tier)
    return header + "\n" + dumps_toml(base), effective


def _generate_gateway_toml(tier: str) -> str:
    """Generate ~/.arc/gateway.toml content for a given tier."""
    lines = [
        _USER_CONFIG_HEADER.format(pkg="gateway", tier=tier),
        "[gateway]",
        f'tier = "{tier}"',
        'agent_did = ""',
        "",
        "[security]",
        "require_pairing = true",
        "",
        "[platforms.telegram]",
        "enabled = false",
        'bot_token_env = "TELEGRAM_BOT_TOKEN"',
        "",
        "[platforms.slack]",
        "enabled = false",
        'bot_token_env = "SLACK_BOT_TOKEN"',
        "",
        "[pairing]",
        "ttl_seconds = 600",
        "",
    ]
    return "\n".join(lines) + "\n"


def _check_provider_key(provider: str) -> bool:
    """Check if the provider's API key env var is set."""
    env_var = _PROVIDER_ENV_VARS.get(provider, "")
    if not env_var:
        return True
    return bool(os.environ.get(env_var, ""))



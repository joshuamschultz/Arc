def _build_parser() -> argparse.ArgumentParser:
    """Build the argparse parser for `arc init`."""
    parser = argparse.ArgumentParser(
        prog="arc init",
        description="Initialize Arc — configure deployment tier and write config files.",
        add_help=True,
    )
    parser.add_argument(
        "--tier",
        choices=_VALID_TIERS,
        default=None,
        help="Deployment tier (personal/enterprise/federal).",
    )
    parser.add_argument(
        "--dir", dest="config_dir", default=None, help="Config directory (default: ~/.arc)."
    )
    parser.add_argument("--provider", default=None, help="Default LLM provider.")
    parser.add_argument("--quick", action="store_true", help="Quick start — personal tier.")
    parser.add_argument(
        "--blueprint",
        default=None,
        help="Bootstrap from a preset blueprint (deep-merged UNDER init defaults).",
    )
    return parser


def init_handler(args: list[str]) -> None:
    """Top-level handler for `arc init [args]`.

    Called by arccli.commands.registry when the user runs `arc init ...`.
    """
    parser = _build_parser()
    parsed = parser.parse_args(args)
    _init(parsed)

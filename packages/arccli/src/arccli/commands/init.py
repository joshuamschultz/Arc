def _init(args: argparse.Namespace) -> None:
    """Initialize Arc — configure deployment tier and write config files."""
    tier: str | None = getattr(args, "tier", None)
    config_dir: str | None = getattr(args, "config_dir", None)
    provider: str | None = getattr(args, "provider", None)
    quick: bool = getattr(args, "quick", False)
    blueprint: str | None = getattr(args, "blueprint", None)

    _write("")
    _write("  Arc — Security-First Agent Framework")
    _write("  ====================================")
    _write("")

    if quick:
        tier = "personal"
    elif tier is None:
        _write("  How do you want to deploy?")
        _write("")
        _write("  1) quick start  — Minimal config, create an agent immediately")
        _write("  2) personal     — No security modules, basic defaults")
        _write("  3) enterprise   — Telemetry, audit, rate limiting, retry + fallback")
        _write("  4) federal      — Full security: routing, PII, signing, OTel, audit")
        _write("")
        choice = input("  Select [1/2/3/4] (default: 1): ").strip() or "1"
        tier_map = {"1": "personal", "2": "personal", "3": "enterprise", "4": "federal"}
        tier = tier_map.get(choice, "personal")
        quick = choice == "1"

    if provider is None:
        _write("")
        _write("  LLM Providers:")
        _write("    Cloud:  anthropic, openai, google, groq, mistral, deepseek")
        _write("    Local:  ollama, lmstudio (no API key needed)")
        _write("")
        provider = input("  Default provider (default: anthropic): ").strip() or "anthropic"
        if provider not in _VALID_PROVIDERS:
            sys.stderr.write(f"Error: Unknown provider '{provider}'.\n")
            sys.exit(1)

    arc_dir = Path(config_dir) if config_dir else Path.home() / ".arc"
    arc_dir.mkdir(parents=True, exist_ok=True)

    try:
        arcagent_content, effective_tier = _generate_arcagent_toml(tier, blueprint)
    except (FileNotFoundError, ValueError) as exc:
        sys.stderr.write(f"Error: {exc}\n")
        sys.exit(1)

    targets: list[tuple[Path, str]] = [
        (arc_dir / "arcllm.toml", _generate_arcllm_toml(tier, provider)),
        (arc_dir / "arcagent.toml", arcagent_content),
        (arc_dir / "gateway.toml", _generate_gateway_toml(tier)),
    ]

    existing = [p for p, _ in targets if p.exists()]
    if existing and not quick:
        names = ", ".join(p.name for p in existing)
        ans = input(f"  These files already exist: {names}. Overwrite? [y/N]: ").strip().lower()
        if ans not in ("y", "yes"):
            _write("  Aborted.")
            return

    for path, content in targets:
        path.write_text(content, encoding="utf-8")

    env_path = arc_dir / ".env"
    if not env_path.exists():
        env_path.touch(mode=0o600)

    # SPEC-053 — the deployment operator key (audit authority for every WORM
    # chain), distinct from any agent DID. Generated once, idempotent. All
    # crypto is delegated to arctrust. Enterprise/federal print the pubkey
    # fingerprint for out-of-band recording (anti-genesis-substitution +
    # witness bootstrap); personal is silent/zero-config.
    from arccli.commands.operator import ensure_operator_key

    operator = ensure_operator_key(arc_dir)
    if tier in ("enterprise", "federal"):
        _write(f"  Operator key fingerprint: {operator.public_key.hex()[:16]}")

    if tier in ("enterprise", "federal"):
        team_dir = arc_dir / "team"
        for sub in ("entities", "channels", "cursors"):
            (team_dir / sub).mkdir(parents=True, exist_ok=True)

    key_ok = _check_provider_key(provider)
    env_var = _PROVIDER_ENV_VARS.get(provider, "")

    tier_display = tier if effective_tier == tier else f"{effective_tier} (raised from {tier})"
    summary = [
        ("Tier", tier_display),
        ("Provider", provider),
        ("Config dir", str(arc_dir)),
        ("Files", ", ".join(p.name for p, _ in targets)),
        ("API key", "OK" if key_ok else f"MISSING ({env_var})"),
    ]
    if blueprint:
        summary.insert(1, ("Blueprint", blueprint))

    _write("")
    _write("  Configuration Summary:")
    _print_kv(summary)

    if not key_ok:
        _write("")
        _write("  Set your API key:")
        _write(f"    export {env_var}=<your-key>")
        _write("")
        _write(f"  Or add it to {env_path}:")
        _write(f"    echo '{env_var}=sk-...' >> {env_path}")

    _write("")


